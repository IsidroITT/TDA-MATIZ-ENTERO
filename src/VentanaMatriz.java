    public void  MostrarMatrizTerminal() {
        for (int i = 0; i < MatrizCua.getTam(); i++) {
            for (int j = 0; j < MatrizCua.getTam(); j++) {
                System.out.print("");
                System.out.print("[" + MatrizCua.getMatriz()[i][j] + "] ");
            }//For interno
            System.out.println("");
        }//For externo
    }//Mostrar matriz en  terminal
    
    public void MostrarMatriz(){
        for (int i = 0; i < MatrizCua.getTam(); i++) {
            for (int j = 0; j < MatrizCua.getTam(); j++) {
                Matriz.setValueAt(MatrizCua.getMatriz()[i][j], i, j);
            }
        }
    }
